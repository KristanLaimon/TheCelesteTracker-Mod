    public class SessionRAMData
    {
        public Database.SaveData? CurrentSaveData = null;
        public Campaign? CurrentCampaign = null;
        public Chapter? CurrentChapter = null;
        public GameSession? CurrentSession = null;
        public global::Celeste.Level? CurrentPlayingLevelArea;
